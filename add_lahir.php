<?php
// Start session
session_start();

// Check if user is logged in
if (!isset($_SESSION['user_id'])) {
    header("Location: login.php");
    exit;
}

// Koneksi ke database
$conn = new mysqli("localhost", "root", "", "project_web");

// Cek koneksi
if ($conn->connect_error) {
    die("Koneksi gagal: " . $conn->connect_error);
}

// Proses data form ketika tombol submit diklik
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $nama = $_POST['nama'];
    $tanggal_lahir = $_POST['tanggal_lahir'];
    $jenis_kelamin = $_POST['jenis_kelamin'];
    $nama_ayah = $_POST['nama_ayah'];
    $nama_ibu = $_POST['nama_ibu'];

    // Query Insert
    $sql = "INSERT INTO kelahiran (nama, tanggal_lahir, jenis_kelamin, nama_ayah, nama_ibu) 
            VALUES ('$nama', '$tanggal_lahir', '$jenis_kelamin', '$nama_ayah', '$nama_ibu')";

    if ($conn->query($sql) === TRUE) {
        echo "<script>alert('Data kelahiran berhasil ditambahkan!'); window.location.href = 'data_lahir.php';</script>";
    } else {
        echo "Error: " . $sql . "<br>" . $conn->error;
    }
}

